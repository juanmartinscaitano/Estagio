public class DescubaLogicaCompletar {
    public static void main(String[] args) {
        System.out.println("Logica da questão a) é adicionar 2 ao próximo número, sendo assim o valor do próximo elemento seria: 9");
        //a)
        System.out.println("Logica da questão b) é  multiplicar por 2 o próximo número, sendo assim o valor do próximo elemento seria: 128");
        //b)
        System.out.println("Logica da questão c) é somar o próximo numero ultilizando apenas números primos, sendo assim o valor do próximo elemento seria: 49");
        //c)
        System.out.println("Logica da questão d) é adicionar no primeiro numero 12(os proximos irao adicionar 8 em cada repeticao" +
                " sendo ex: 12, 12+8, 20+8,28+8) sendo a resposta o número: 100");
        //d)
        System.out.println("Logica da questão e) é ultilizar o método fibonacci, sendo assim o valor do próximo elemento seria: 13");
        //e)
        System.out.println("Logica da questão f) é utilizar a seuquência '+10, +2, +4, +1, +1, +1' (os próximos números seriam a soma do anterior + 1), sendo assim o valor do próximo elemento seria: 20");
        //f)
    }
}


